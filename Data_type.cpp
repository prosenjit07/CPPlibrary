#include <bits/stdc++.h>
using namespace std;
int main()
{
    short a;
    int b;
    long c;
    long long d;
    // short <= int <= long <= long long
    unsigned short aa;
    unsigned int bb;
    unsigned long cc;
    unsigned long long dd;

    cout << sizeof(a)<<endl;
    cout << sizeof(b)<<endl;
    cout << sizeof(c)<<endl;
    cout << sizeof(d)<<endl;
    cout << sizeof(aa)<<endl;
    cout << sizeof(bb)<<endl;
    cout << sizeof(cc)<<endl;
    cout << sizeof(dd)<<endl;
    return 0;
}